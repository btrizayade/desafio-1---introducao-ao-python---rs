# inteiro
numero_inteiro = 3
print("inteiro = ", numero_inteiro)

# real com ponto flutuante
numero_real = 3.14
print("real com ponto flutuante = ", numero_real)

# type()
print("tipo de variável inteiro = ", type(numero_inteiro))
print("tipo de variável inteiro = ", type(numero_real))

# soma +
soma = 1 + 1
print("1 + 1 = ", soma)

# subtração -
subtracao = 1 - 1
print("1 - 1 = ", subtracao)

# multiplicação *
multiplicacao = 2 * 2
print("2 * 2 = ", multiplicacao)

# divisao / (resultado sempre float)
divisao = 5 / 2
print("5 / 2 = ", divisao)

# divisao inteiro)
divisao = 5 // 2
print("5 // 2 = ", divisao)

# int()
print("valor inteiro = ", int(divisao))

# float()
print("valor em float = ", float(soma))

# módulo %
modulo = 5 % 2
print("moduo = ", modulo)